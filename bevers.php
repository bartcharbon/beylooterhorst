                    Algemeen De kinderen van 5 t/m 7 jaar worden bij Scouting bevers genoemd.<br> Bij de bevers ligt de nadruk op het spelen in de natuur. Ze doen samen met andere kinderen leuke spelletjes in een veilige omgeving.<br> Zo leren ze spelenderwijs samen te werken onder leiding van deskundige vrijwilligers.<br> De bevers spelen samen met stuiter in het themadorp Hotsjietonia en gebruiken hierbij hun fantasie. Opkomsten<br> Wij beginnen op zaterdag om 9.30 samen met de hele groep bij de vlaggenmast. Als de vlaggen wapperen gaan we naar onze beverruimte waar we openen met het beverlied.<br> Als we gekeken hebben wie er allemaal zijn dan gaan we van start.<br> De ene keer gaan we met Keet en Fleur op stap, de andere keer met Bas en Rebbel.<br> Soms gaan we een paar weken achter elkaar aan de slag voor een beverbadge. Maar het kan ook zijn dat we iets samen met de welpen gaan doen of mee doen aan bijvoorbeeld de JOTA ( Jamboree On The Air)<br> of de beverdoedag waar alle bevergroepen uit regio Drenthe samen gaan spelen.
