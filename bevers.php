                    Kampen Natuurlijk gaan de bevers ook op kamp, maar niet zo lang hoor. In september doen we mee met het startkamp van de groep en dan slapen we gewoon in de beverruimte.<br> In januari hebben we een weekendkamp alleen met de bevers en in mei gaan we nog een keer op weekendkamp.<br> Dan gaan we twee nachtjes in een ander clubhuis logeren.<br> Tijdens zo'n weekend beleven de bevers allerlei leuke avonturen in een speciaal thema. Van te voren krijg je altijd een brief met daarin de informatie over het kamp en wat gaat kosten.<br> Wij vinden het heel belangrijk dat iedereen met ons mee kan op kamp.<br> Als deze extra kosten de reden zijn om niet mee te gaan, schroom dan niet en neem even contact op met de leiding.
