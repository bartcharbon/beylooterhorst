		<strong>De geschiedenis van Beyloo Ter Horst:(door Wim van Eisden)</strong>
		<br>
		<br />
		<br />
		<IMG SRC="images/DSCF2643.JPG" ALIGN="LEFT" />De alleroudste
		Scoutingsporen - liever Padvindersspoor - gaat terug naar het jaar
		1917. Onder leiding van de toenmalig burgemeester heeft deze groep ,
		waarvan de naam onbekend is , slechts enkele jaren gedraaid. Ze
		zorgden echter wel voor een soort revolutie in het zo rustige Drentse
		dorp. <br />
		<br />
		De eerstvolgende reguliere groep werd opgericht eind 1967 en bestond
		eigenlijk uit twee groepen , de "Horstgroep" , aangesloten bij het NPG
		(Ned.Padvindsters Gilde) en de "Eekschellers" aangesloten bij het NPV
		(Ned.Padvinders Vereniging). De eerste naam vinden we ook terug in de
		naam van de huidige Scoutinggroep nl. "Beyloo Ter Horst" , waarin dat
		Ter Horst te maken heeft met de buurtschap Ter Horst , de plaats waar
		het groepshuis gevestigd is. <br />
		<br />
		De verklaring van de tweede naam is iets ingewikkelder ; Eekschellers
		waren rondtrekkende families die zich in de Drentse dreven bezig
		hielden met het schillen van jonge eikeboompjes en die deze schil
		verkochten aan de leerlooierijen. Van het overgebleven hout werd
		houtskool gemaakt. Je ziet het, de scoutingelementen : primitief
		(kamperend !), rondtrekken (vgl. hikes !), houtwerk (pionieren),
		leefgemeenschap ( de Troep), vuur, zijn overduidelijk aanwezig. Jammer
		eigenlijk dat zo'n mooie naam verloren moest gaan , maar het samengaan
		van jongens en meisjesgroep in 1989 maakte een nieuwe naam
		noodzakelijk. <br />
		<br />
		De Welpen en Kabouters waren het eerst aan de beurt en daarna volgden
		natuurlijk de Verkenners en de Padvindsters. Op de leeftijd van zo'n
		jaar of vijftien hield het op , Voortrekkers hebben nooit bestaan. <br />
		<br />
		Via allerlei onderkomens ( oude school , gasgebouwtje , werkkeet,
		kleuterschool) is de groep eindelijk in het jaar 1975 beland op de
		huidige lokatie , Smalbroek 25, nabij het natuurgebied het
		Terhorsterzand. Een keuze waar we de toenmalige commissie wel zeer
		dankbaar voor mogen zijn ; een betere locatie is haast niet denkbaar.
		Het achterste gedeelte van een gemeenteboerderij moest eerst geheel
		worden opgeknapt. De stallen moesten eruit, de "grup" gedicht alles
		betimmerd, een buitentrap aangelegd, want er moest boven ook worden
		geslapen. Provinciale- gemeentelijke- en rijkssubsidie hebben dit
		allemaal mogelijk gemaakt. En vanzelfsprekend zeer veel
		zelfwerkzaamheid. <br />
		<br />
		<IMG SRC="images/DSCF2603.JPG" ALIGN="RIGHT" />Tien jaar daarna , in
		1985 , werd de losstaande vervallen kapschuur aan de accommodatie
		toegevoegd, toen echter met aanzienlijk minder subsidie! En nu alweer
		is de ruimte eigenlijk al weer te klein voor de nu bestaande
		spelgroepen te weten , Welpen, Scouts, Explorers, Stam en Gug. Echter,
		er wordt al weer aan uitbreiding gewerkt! <br />
		<br />
		Wat het ledental betreft, de meisjesgroep heeft in ledental de
		jongensgroep steeds verre overtroffen ; op een gegeven moment kende
		alleen deze meisjesgroep al 120 leden ! Ter vergelijking , het huidige
		ledental ligt zo ongeveer op hetzelfde aantal. <br />
		Van meetafaan heeft de groep zich bijzonder ingespannen om de nodige
		centjes bij elkaar te krijgen. Vergeet niet dat het na oprichting
		so-wie-so al jaren duurt voor je wat eigen kampeermateriaal bij elkaar
		hebt. <br />
		<br />
		De gevoerde akties waren achtereenvolgens : oud papier, zelfgemaakte
		snert, 200 liter warm uitgevent ( 8 jaren), een kerstboomaktie ( 18
		jaren !) , zelf gebakken kniepertjes ( 3 jaren) een mestaktie (slechts
		1 jaar), de Kaapse Viooltjes bloemenaktie ( 8-tal jaren), en nu dus de
		Eieractie. <br />
		<br />
		Kampen en trektochten zijn er natuurlijk ook legio geweest. We hoeven
		alleen maar te denken aan Ommen, Steenwijk, Terschelling, de
		Achterhoek en niet te vergeten alle NPK's en PKD's te Anloo en
		Dwingeloo. Wat het buitenland betreft : Zuid Engeland, Belgische
		Ardennen en Luxemburg. Zowel voet- als fietstochten. <br />
		<br />
		Je ziet het, Scouting leeft in Beilen en Beilen leeft nog steeds met
		Scouting!</div>
