		vuurtonnen kan worden gezorgd. Voor de tarieven
		van ons terrein kunt u contact met ons opnemen.<br>
		<br>
		Gebouwen en terrein vormen zo een unieke gelegenheid voor een
		gezamenlijk kamp van een<br>
		gehele scoutinggroep waar alle speltakken tot hun recht kunnen komen.
		<br>
		Om een grote groep nog meer van dienst te kunnen zijn beschikken wij
		nog over een aparte kookgelegenheid waar voor wel 100 personen gekookt
		kan worden. Het voormalige stookhok van de boerderij is hiertoe
		omgebouwd en voorzien van supergrote branders. De benodigde grote
		pannen voor zo’n groep zijn vanzelfsprekend ook beschikbaar.<br>
		En voor wie het wat eenvoudiger wil doen , wij kunnen zorgen voor een
		kooktent plus toebehoren, zoals gas en pannen.<br>
