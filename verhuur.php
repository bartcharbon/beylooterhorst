		<u><strong>Het kampeerterrein.</strong><br>
		</u>Aansluitend aan het speelveld bevinden zich twee kleinere of één
		groot kampeerterrein , totaal ongeveer 0,8 ha groot. Daarnaast&nbsp;is
		er een toiletunit met 2 toiletten 2 douchen en wasgelegenheid. Het
		terrein is omzoomd door lage bosjes met aan één zijde een bosgebied.
		Er mag onder voorwaarden op houtvuur gekookt worden en stookhout is te
		verkrijgen. <br>
		<img src="images/verhuur/liveearth3d.jpg" border="0" alt=""><br>
