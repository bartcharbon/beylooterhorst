		<strong>De gebouwen.</strong><br>
		</u>De accommodatie bestaat uit twee gebouwen , Oelebred en Kapschuur
		genaamd.<br>
		Het Oelebred bestaat uit een grote leefruimte op de begane grond en
		voorzien van centrale verwarming en een open haard. Deze ruimte kan
		voor ongeveer 40 personen dienen als dagverblijf. Verder zijn er twee
		toiletten aanwezig.<br>
		Annex bevindt zich een goed ingerichte keuken met kookinventaris,
		inclusief borden, kopjes en bestek.<br>
		In de losstaande Kapschuur bevinden zich ruimtes beneden en op de
		eerste verdieping. Dit is het gebouw waar geslapen mag worden.
		Matrassen, kussens, lakens en slaapzak moeten zelf worden meegenomen.
		Een wasruimte voorzien van&nbsp;1 toilet en 2 douchen zijn aanwezig.<br>
		<br>
		Bij de gebouwen behoort ook een speelveld en een kampvuurplaats.<br>
		Alles bij elkaar een zeer geschikte groepsaccommodatie voor
		scoutinggroepen, scholen, verenigingen en familie dag/weekends.<br>
