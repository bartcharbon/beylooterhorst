                   Aanmelden scouts
Als je als jongen lid wil worden van de scouts kom je eerst op de wachtlijst te staan. Hoelang je hier opstaat is lastig te zeggen. Wanneer wij weer een plaatsje vrij hebben nemen wij contact met je op zodat je 3x gratis kunt komen kijken.<br/> 
Wil je als meisje lid worden van de scouts heb je een klein voordeel, hiervoor hebben wij namelijk geen wachtlijst en kun je zo binnen stromen. Mocht je  willen komen kijken neem dan contact met ons op.<br/><br/>

