            			<b>Uniform</b><br/>
			- Altijd volledig uniform (blouse en das)<br>
			- Alleen scouting-insignes - bij twijfel overleggen met de leiding<br>
			- Geen grote platen op de rug<br>
			- <a href="http://nl.scoutwiki.org/Afbeelding:Uniform_scouts_sn_2010.png" target="_blank">Uniform plaatje van scouting Nederland</a><br>
